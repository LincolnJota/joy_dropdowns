/// Overlay position enum. Used in the ``overlayPosition`` property of the JoyDropDown widget.
/// The available ones are:`
/// * ``JoyOverlayPosition.top``
/// * ``JoyOverlayPosition.bottom``
/// * ``JoyOverlayPosition.left``
/// * ``JoyOverlayPosition.right``
enum JoyOverlayPosition {
  top,
  bottom,
  left,
  right,
}
